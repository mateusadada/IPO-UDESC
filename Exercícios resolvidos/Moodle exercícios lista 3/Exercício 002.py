# Crie uma lista de nomes e ordene em ordem alfabética.

print('Bem vindo ao programa que ordena os nomes de uma lista!')

nomes = ['casa', 'bola', 'tesoura', 'mansão', 'tecnologia', 'universidade', 'arte', 'design', 'moldura']

print(f'Nomes: {nomes}')

nomes.sort()

print(f'\nNomes crescentes: {nomes}')
