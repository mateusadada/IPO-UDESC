# Crie uma lista de palavras e concatene todas as palavras em uma única string.

print('Bem vindo ao programa que junta todas as palavras de uma lista em uma string!')

lista = ['casa', 'bola', 'tesoura', 'mansão', 'tecnologia', 'universidade', 'arte', 'design', 'moldura']
concatenada = ' '.join(lista)

print(f'Lista: {lista}'
      f'\n\nÚnica string: {concatenada}')
